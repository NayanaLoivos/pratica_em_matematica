//O QUE É PORCENTAGEM?
    //Podemos entender um valor em porcentagem como uma fração cujo denominador é 100 
    //e o numerador é igual ao valor da porcentagem. Por exemplo, 10% corresponde a fração 10/100.
    //Porcentagem assim como fração é uma forma de termos uma medida relativa de algo.
    //Exemplo, se vemos que o PIB do brasil está previsto para crescer 3% nesse ano, 
    //estamos falando implicitamente que é 3% em relação ao valor anterior do ano passado.
    //Só iremos extrair um significado absoluto desse exemplo quando tivermos o número 
    //de fato de quanto foi o PIB no ano passado. Por ser uma medida relativa 3% do PIB do Brasil, 
    //será diferente de 3% do PIB da China e assim sucessivamente a outros Países.

    //Outro exemplo, supondo que estamos querendo saber quanto é o desconto de 10% de um produto que custa R$100,00.
    //10% de 100.
    //Uma porcentagem pode ser vista como uma fração que tem o denominador 100, ou seja, a fração que
    //representa 10% nesse caso é 10/100, então 10% de 100 reais é pegar essa fração 10/100 e
    //multiplicá-la pelo valor que queremos verificar, ou seja, 100. Portanto:
    //10/100 * 100 = 10/1 * 1/1 = 10.
    //Então 10% em cima de 100 reais é 10 reais, logo se seu desconto é de 10%,
    //você tem 90 reais a pagar.

    //E SE APLICAMOS O INVERSO DO EXEMPLO ACIMA?
        //Digamos agora que quero vender esse produto que comprei por R$90,00.
        //E para vender temos impostos à pagar sobre a venda, portanto temos que calcular o valor o qual teremos
        //que aumentar esse produto para passar a outro consumido. Aumentando 10% temos
        //10/100 * 90 = 9.
        //Repara que se aumentarmos um produto que custa R$90,00 em 10% no total 
        //obteremos R$99,00 como valor final (90 + 9), ao passo que se damos desconto de 10% num produto 
        //de R$100,00 obteremos R$90,00.
        //O QUE PODEMOS CONCLUIR?
            //Que se olhar para o 90 e aumentarmos 10% é diferente de olharmos para o 100 
            //e descontar 10%. Por isso a porcentagem é uma medida totalmente relativa, pois, 
            //se tomamos uma porcentagem em relação a valores diferentes, teremos medidas
            //totalmente diferentes relativas aos valores em questão.

    //FRAÇÃO É UMA RAZÃO ENTRE NÚMEROS INTEIROS?
        //Exatamente. Portanto se a fração é uma razão entre dois números inteiros temos que:
        //7,5% de 1100
        //7,5% não é inteiro, portanto se queremos olhar para 7,5% como uma fração
        //devemos multiplicar e dividir o denominador por um mesmo valor até termos uma razão entre inteiros.
        //Se multiplicamos e divimos por 10, o resultado é 75/1000.
        //Portanto, 7,5/100 equivale 75/1000 que é uma representação válida para essa fração,
        //que quando simplificada teremos 3/40.
        //Portanto 7,5% é equivalente a 3/40.
        //Se olhar para a fração 7,5/100 vemos que temos que multiplicar 7,5 e dividir por 100, 
        //então o próprio numerador está te dando o valor que se deve multiplicar e o denominador 
        //o valor que você tem que dividir. E para todas as porcentagens será assim.
        //Mesmo sabendo que 3/40 é equivalente a 7,5%, para resolvermos a questão
        //é mais fácil olharmos para 7,5/100 do que para 3/40.

        //EXISTE OUTRA MANEIRA DE OLHAR?
            //Se paramos para pensar se divirmos 1100 por 100 obteremos 11, e logo podemos fazer 7,5 * 11 = 82.5.
            //O resultado será o mesmo se realizamos a conta 7,5/100 * 1100 = 7,5 * 11 = 82.5.
            //Ou ainda 30/4 * 1100 = 330/4 = 82.5.
            //Se esse desconto fosse relativo ao pagamento da Previdência, teríamos que o salário líquido da pessoa
            //é de 1100 reais, o desconto da Previdência é 82,5 e o salário líquido da pessoa seria 1017,5.

